package Array1;
import java.util.*;

public class Array9064_ex1_2 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int[] array = new int[5];
        for(int i=0; i<5; i++) {
            array[i] = sc.nextInt();
            System.out.print(array[i] + " ");
        }

        sc.close();
    }
}
