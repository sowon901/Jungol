package Array1;
import java.util.*;

public class Array9063_ex1_1 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int[] arr = new int[5];

        for(int i=0; i<5; i++) {
            arr[i] = sc.nextInt();
            System.out.print(arr[i] + " ");
        }
    }
}
