package loop2;
import java.util.*;

public class Loop9053_ex7 {
    public static void main(String[] args) {
        for(int i=1; i<=5; i++) {
            for(int j=1; j<=5; j++) {
                System.out.print(i+" ");
            }
            System.out.println();
        }
        System.out.println();

        for(int i=1; i<=5; i++) {
            for(int j=1; j<=5; j++) {
                System.out.print(j+" ");
            }
            System.out.println();
        }
    }
}
